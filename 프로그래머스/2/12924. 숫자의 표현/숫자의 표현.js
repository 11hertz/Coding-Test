function solution(n) {
    let answer = 0;
  
    for(let i = 1; i <= n; i++) {
        if(n % i === 0 && i % 2 === 1) answer++;
    }

    // 1 [1]
    // 2 [2]
    // 3 [3], [1, 2]
    // 4 [4]
    // 5 [5], [2, 3]
    // 6 [6], [1, 2, 3]
    // 7 [7], [3, 4]
    // 8 [8]
    // 9 [9], [2, 3, 4], [4, 5]
    // 10 [10], [1, 2, 3, 4]
    // 11 [11], [5, 6]
    // 12 [12], [3, 4, 5]
    // 13 [13], [6, 7]
    // 14 [14], [2, 3, 4, 5]
    // 15 [15], [1, 2, 3, 4, 5], [4, 5, 6], [7, 8]
    // 30 [30], [4, 5, 6, 7, 8], [6, 7, 8, 9], [9, 10, 11]
    
    
    return answer;
}